package com.techelevator.echo.model;

public class Echo {
	
	private int startingFontSize;
	
	
	private String word;


	/**
	 * @return the startingFontSize
	 */
	public int getStartingFontSize() {
		return startingFontSize;
	}


	/**
	 * @param startingFontSize the startingFontSize to set
	 */
	public void setStartingFontSize(int startingFontSize) {
		this.startingFontSize = startingFontSize;
	}


	/**
	 * @return the word
	 */
	public String getWord() {
		return word;
	}


	/**
	 * @param word the word to set
	 */
	public void setWord(String word) {
		this.word = word;
	}
	
	
	
	
	
	
}
